import 'package:flutter/material.dart';

TextStyle notoSansBoldFont(double size, Color color) {
  return TextStyle(
      fontSize: size,
      fontFamily: 'NotoSans',
      fontWeight: FontWeight.bold,
      color: color);
}

TextStyle notoSansRegularFont(double size, Color color) { 
  return TextStyle(
      fontSize: size,
      fontFamily: 'NotoSans',
      color: color);
}